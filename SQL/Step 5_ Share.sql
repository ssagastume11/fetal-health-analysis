-- Objective:

-- The goal of this step is to communicate fetal health analysis information 
-- clearly, accessibly, and practically to both technically and non-technically knowledgeable 
-- audiences.
-- By transforming aggregated CTG metrics into easily visualized results, this 
-- step facilitates comparison between normal, suspicious, and pathological fetal health 
-- classifications and helps identify patterns that can guide clinical follow-up 
-- and intervention decisions.

-- Key Actions:

-- * Use the analysis-level view to summarize key metrics such as fetal heart rate, variability, deceleration, and histogram according to fetal health classification.
-- * Prepare clear and well-labeled results suitable for charts, dashboards, and stakeholder reports.
-- * Enable rapid identification of trends and differences between risk groups through visual comparisons.
-- * Support post-processing visualization tools (BigQuery charts, Looker Studio, Tableau) without requiring additional data transformation.
-- * Present findings in a way that aligns with healthcare decision-making and public health communication objectives.